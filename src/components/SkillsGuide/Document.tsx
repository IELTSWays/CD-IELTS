export const DocSpeaking = () => {
  return (
    <Typography variant="body2">
      <ul id="list-red-bullet">
        <li>
          <strong>The IELTS Speaking test</strong> is the same for both the Academic and General Training versions of the IELTS exam.
        </li>
        <li>
          The test has three parts and lasts for 11 to 14 minutes.
        </li>
        <li>
          Part 1: Introduction and interview. You will answer some general questions about yourself and your family, your work or studies, your hobbies and interests, and your hometown or country. This part lasts for 4 to 5 minutes.
        </li>
        <li>
          Part 2: Individual long turn. You will be given a topic card with a question and some points to cover. You will have one minute to prepare and then you will speak for up to two minutes on the topic. The examiner may ask you one or two follow-up questions. This part lasts for 3 to 4 minutes.
        </li>
        <li>
          Part 3: Two-way discussion. You will have a discussion with the examiner on some abstract issues related to the topic in Part 2. You will be expected to express and justify your opinions, compare and contrast different views, and speculate about possible outcomes. This part lasts for 4 to 5 minutes.
        </li>
        <li>
          You will be graded on four criteria: fluency and coherence, lexical resource, grammatical range and accuracy, and pronunciation.
        </li>
      </ul>
    </Typography>
  )
};
